void CRC_Init(void){

	RCC_voidEnablePeripheralClock(RCC_AHB, RCC_AHB_CRCEN);

}

void CRC_RESET_DR(void){

	SET_BIT(CRC->CRC_CR, CRC_RESET_BIT_POS);

}

u32 CRC_Accumlate(u32 Arr_Data[], u32 Copy_u32ArrSize){

	u32 temp = 0;
	u32 index = 0;
	for(index=0;index<Copy_u32ArrSize;index++){

		CRC->CRC_DR = Arr_Data[index];

	}
	temp = CRC->CRC_DR;
	return temp;
}


























